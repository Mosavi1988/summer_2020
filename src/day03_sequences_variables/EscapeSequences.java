package day03_sequences_variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \nMy name is Cybertek");
        System.out.println(" \tI am in Virginia\n");
        System.out.println("\t\t\tI love programming");
        System.out.println("\\\\");
        System.out.println("//");

        System.out.println("My name is \"Hassan\"");
        System.out.println("My name is 'Hassan'");

        System.out.println("My favorite TV-Series: \"Game of Throne\"");

        String result = "Hassan";
        System.out.println("My name is "+result);
    }
}
