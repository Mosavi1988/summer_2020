package day22_NestedLoop;

public class NestedLoop3 {
    public static void main(String[] args) {



        int j = 1;
        while (j <= 10){
            for(int i = 1; i <= 20; i++){
                System.out.print("*");

            }
            System.out.println();


            j++;
        }



    }
}
